int		ft_str_is_lowercase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ('a' <= str[i] && 'z' >= str[i])
        {
            i++;
        }
        else
        {
            return (0);
        }
	}
	return (1);
}


